package com.kuycoding.wijayafarm.model;

public class Ayam {
    private String uid;
    private String create_at;
    private String jumlah;
    private String hidup;
    private String mati;
    private String sehat;
    private String sakit;
    private String sembuh;
    private String periode;
    private String next_periode;
    private String tersedia;
    private String terjual;

    public Ayam() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getCreate_at() {
        return create_at;
    }

    public void setCreate_at(String create_at) {
        this.create_at = create_at;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getHidup() {
        return hidup;
    }

    public void setHidup(String hidup) {
        this.hidup = hidup;
    }

    public String getMati() {
        return mati;
    }

    public void setMati(String mati) {
        this.mati = mati;
    }

    public String getSehat() {
        return sehat;
    }

    public void setSehat(String sehat) {
        this.sehat = sehat;
    }

    public String getSakit() {
        return sakit;
    }

    public void setSakit(String sakit) {
        this.sakit = sakit;
    }

    public String getSembuh() {
        return sembuh;
    }

    public void setSembuh(String sembuh) {
        this.sembuh = sembuh;
    }

    public String getPeriode() {
        return periode;
    }

    public void setPeriode(String periode) {
        this.periode = periode;
    }

    public String getNext_periode() {
        return next_periode;
    }

    public void setNext_periode(String next_periode) {
        this.next_periode = next_periode;
    }

    public String getTersedia() {
        return tersedia;
    }

    public void setTersedia(String tersedia) {
        this.tersedia = tersedia;
    }

    public String getTerjual() {
        return terjual;
    }

    public void setTerjual(String terjual) {
        this.terjual = terjual;
    }
}
